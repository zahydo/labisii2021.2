    private void btnAddFoodActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddFoodActionPerformed
        IFoodAccess service = Factory.getInstance().getFoodService();
        // Inyecta la dependencia
        FoodService foodService = new FoodService(service);

        Food food = new Food();
        food.setId(txtFoodId.getText());
        food.setName(txtFoodName.getText());
        food.setDescription(txtFoodDescription.getText());


        try {
            String response = foodService.createFood(food);
            successMessage("Comida "+ response + " agregado con éxito.", "Atención");
            clearData();
            txtFindFoodId.setText("");
            btnAddFood.setVisible(false);

        } catch (Exception ex) {
            successMessage(ex.getMessage(), "Atención");
        }
    }//GEN-LAST:event_btnAddFoodActionPerformed


    private void clearData() {

        //txtFindFoodId.setText("");
        txtFoodId.setText("");
        txtFoodName.setText("");
        txtFoodDescription.setText("");

    }   

    private void showData (Food data) {
        txtFoodId.setText(data.getId());
        txtFoodName.setText(data.getName());
        txtFoodDescription.setText(data.getDescription());
    }

