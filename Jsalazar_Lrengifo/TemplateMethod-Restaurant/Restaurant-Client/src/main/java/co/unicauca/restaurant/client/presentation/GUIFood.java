    private void btnBuscarFoodActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarFoodActionPerformed
        String id = txtFindFoodId.getText().trim();
        IFoodAccess service = Factory.getInstance().getFoodService();

        FoodService foodService = new FoodService(service);
        if (id.equals("")) {
            btnAddFood.setVisible(false);
            txtFoodName.requestFocus();
            return;
        }

        Food food;
        try {
            food = foodService.findFood(id);
        } catch (Exception ex) {
            clearData();
            successMessage(ex.getMessage(), "Atención");
            txtFoodId.setText(txtFindFoodId.getText());
            btnAddFood.setVisible(true);
            return;
        }
        clearData();
        showData(food);
    }//GEN-LAST:event_btnBuscarFoodActionPerformed

