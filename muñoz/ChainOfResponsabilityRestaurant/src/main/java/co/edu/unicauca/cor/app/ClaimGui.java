    private void AceptButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AceptButtonActionPerformed
        String tt = titulo.getText();  
        String des = Descripcion.getText();  
        TypeEnum te = (TypeEnum) TiposClaim.getSelectedItem(); 
        if(des.isEmpty() || tt.isEmpty()){
            Messages.warningMessage("todos los campos son obligatorios", "Atenci贸n");
        }else{
            Claim clm = new Claim(0, tt, des, te);
            Messages.successMessage(manager.getLevelOne().attend(clm), "Informaci贸n");
            if(te != TypeEnum.UNCLASSIFIED){
                clearData(); 
            }
        }
    }//GEN-LAST:event_AceptButtonActionPerformed

